    private void btnChangeTableCarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeTableCarActionPerformed
        int result=0;
        switch(evt.getActionCommand()){
            case "insert":
                try {
                    result=addCar(jTextField2.getText(),jTextField3.getText(),jTextField4.getText(),
                        jTextField5.getText(),jTextField6.getText(),jTextField7.getText(),jTextField8.getText());
                } catch (SQLException ex) {
                    Logger.getLogger(InputRouteUI.class.getName()).log(Level.SEVERE, null, ex);
                }
            break;
            case "update":
                String oldNumber=model.getValueAt(jTable1.getSelectedRow(),1).toString();
                    try {
                    result=updateCar(oldNumber,jTextField2.getText(),jTextField3.getText(),jTextField4.getText(),
                            jTextField5.getText(),jTextField6.getText(),jTextField7.getText(),jTextField8.getText());
                } catch (SQLException ex) {
                    Logger.getLogger(InputRouteUI.class.getName()).log(Level.SEVERE, null, ex);
                }
        }
        if(result==0){
                showMessageDialog(jDialog1,"Операція виконана успішно", "Редагування автомобілів",PLAIN_MESSAGE);
                this.jDialog1.setVisible(false);
            }
        else{
            showMessageDialog(jDialog1,"error",
                             "er",ERROR_MESSAGE);
            return;
            }
        try {
            this.showUserCars();
        } catch (SQLException ex) {
            Logger.getLogger(InputRouteUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnChangeTableCarActionPerformed

