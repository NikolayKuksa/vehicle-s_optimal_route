    private int addCar(String name, String num,String hourse,String pcity,String ptrack,String ptot, String engine) throws SQLException{
        CallableStatement stm=conn.prepareCall("{? = call ADD_CAR(?,?,?,?,?,?,?,?)}");
            stm.registerOutParameter (1, Types.INTEGER);
            stm.setString(2, userDriverLicense);
            stm.setString(3,name);
            stm.setString(4, num);
            stm.setFloat(5, Float.parseFloat(hourse));
            stm.setFloat(6, Float.parseFloat(pcity));
            stm.setFloat(7, Float.parseFloat(ptrack));
            stm.setFloat(8, Float.parseFloat(ptot));
            stm.setString(9,engine);
            stm.execute();
            int result=stm.getInt(1);
            stm.close();
            return result;
    }
        private int updateCar(String oldNum,String name, String newNum,String hourse,String pcity,String ptrack,String ptot, String engine) throws SQLException{
        CallableStatement stm=conn.prepareCall("{? = call UPDATE_CAR(?,?,?,?,?,?,?,?,?)}");
            stm.registerOutParameter (1, Types.INTEGER);
            stm.setString(2, userDriverLicense);
            stm.setString(3, oldNum);
            stm.setString(4,name);
            stm.setString(5, newNum);
            stm.setFloat(6, Integer.parseInt(hourse));
            stm.setFloat(7, Float.parseFloat(pcity));
            stm.setFloat(8, Float.parseFloat(ptrack));
            stm.setFloat(9, Float.parseFloat(ptot));
            stm.setString(10,engine);
            stm.execute();
            int result=stm.getInt(1);
            stm.close();
            return result;
    }
